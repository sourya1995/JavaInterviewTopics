package JavaCodingProblems.IO;

public class Club {

    private String name;
    private int numberOfCLs;

    public Club(String name, int numberOfCLs) {
        this.name = name;
        this.numberOfCLs = numberOfCLs;
    }

    public String getName() {
        return name;
    }

    public int getNumberOfCLs() {
        return numberOfCLs;
    }

}
